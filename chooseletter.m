function aa=chooseletter(jj)
% Given an interger jj returns the corresponding letter in the Italian alphabet
switch (jj)
   case 1, aa='A';
   case 2, aa='B';
   case 3, aa='C';
   case 4, aa='D';
   case 5, aa='E';
   case 6, aa='F';
   case 7, aa='G';
   case 8, aa='H';
   case 9, aa='I';
   case 10, aa='L';
   case 11, aa='M';
   case 12, aa='N';
   case 13, aa='O';
   case 14, aa='P';
   case 15, aa='Q';
   case 16, aa='R';
   case 17, aa='S';
   case 18, aa='T';
   case 19, aa='U';
   case 20, aa='V';
   case 21, aa='Z';
end       
